// =============================================================================
public class Counter {
    // =========================================================================
    // /////////////////////////////////////////////////////////////////////////
    // ///// 属性(カウンターの内部状態) ////////////////////////////////////////
    // /////////////////////////////////////////////////////////////////////////
    // =========================================================================
    /**
     * [値属性] カウント値.
     */
    // =========================================================================
    private int count = 0;
    // =========================================================================
    /**
     * [値属性] 上限値.
     */
    // =========================================================================
    private int max = 20;
    // =========================================================================
    /**
     * [値属性] 下限値.
     */
    // =========================================================================
    private int min = 0;
    // =========================================================================
    // /////////////////////////////////////////////////////////////////////////
    // ///// コンストラクタ (初期化) ///////////////////////////////////////////
    // /////////////////////////////////////////////////////////////////////////
    // =========================================================================
    /**
     * [コンストラクタ] 初期化する.
     */
    // =========================================================================
    public Counter() {
        // ---------------------------------------------------------------------
        count = 0;
        // ---------------------------------------------------------------------
        max = 20;
        min = 0;
        // ---------------------------------------------------------------------
    }
    // =========================================================================
    // /////////////////////////////////////////////////////////////////////////
    // ///// インスタンスメソッド //////////////////////////////////////////////
    // /////////////////////////////////////////////////////////////////////////
    // =========================================================================
    /**
     * カウントアップする.
     */
    // =========================================================================
    public void up() {
        // ---------------------------------------------------------------------
        if (count < max) {
            count++;
        } else {
            throw new IllegalStateException(
                String.format("カウントが上限値%dなのにアップさせようとした",max));
        }
        // ---------------------------------------------------------------------
    }
    // =========================================================================
    /**
     * カウントダウンする.
     */
    // =========================================================================
    public void down() {
        // ---------------------------------------------------------------------
        if (count > min) {
            count--;
        } else {
            throw new IllegalStateException(
                 String.format( "カウントが下限値%dなのにダウンさせようとした",min));
        }
        // ---------------------------------------------------------------------
    }
    // =========================================================================
    /**
     * カウントをリセットする.
     */
    // =========================================================================
    public void reset() {
        // ---------------------------------------------------------------------
        count = 0;
        // ---------------------------------------------------------------------
    }
    // =========================================================================
    // /////////////////////////////////////////////////////////////////////////
    // ///// インスタンスメソッド: アクセッサ //////////////////////////////////
    // /////////////////////////////////////////////////////////////////////////
    // =========================================================================
    /**
     * [getter] カウント値を返す.
     *
     * @return カウント値.
     */
    // =========================================================================
    public int getCount() {
        // ---------------------------------------------------------------------
        return (count);
        // ---------------------------------------------------------------------
    }
    // =========================================================================
    /**
     * [setter] カウント値を設定する.
     *
     * @param count 設定するカウント値.
     */
    // =========================================================================
    public void setCount(int count) {
        // ---------------------------------------------------------------------
        if (count >= min && count <=max) {
            this.count = count;
        } else {
            throw new IllegalStateException(
                String.format( "設定しようとする値 %dが下限値%dと上限値%dの間にない", count, min, max));
        }
        // ---------------------------------------------------------------------
    }
    // =========================================================================
    /**
     * [getter] 上限値を返す.
     * @return 上限値
     */
    // =========================================================================
    public int getMax() {
        // ---------------------------------------------------------------------
        return (max);
        // ---------------------------------------------------------------------
    }
    // =========================================================================
    /**
     * [getter] 下限値を返す.
     * @return 下限値
     */
    // =========================================================================
    public int getMin() {
        // ---------------------------------------------------------------------
        return (min);
        // ---------------------------------------------------------------------
    }
    // =========================================================================
    /**
     * [setter] 上限値を設定する.
     * @param max 上限値
     */
    // =========================================================================
    public void setMax(int max) {
        // ---------------------------------------------------------------------
        this.max = max;
        // ---------------------------------------------------------------------
    }
    // =========================================================================
    /**
     * [setter] 下限値を設定する.
     * @param min 下限値
     */
    // =========================================================================
    public void setMin(int min) {
        // ---------------------------------------------------------------------
        this.min = min;
        // ---------------------------------------------------------------------
    }
    // =========================================================================
}
