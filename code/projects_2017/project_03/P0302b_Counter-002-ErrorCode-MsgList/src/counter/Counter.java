// =============================================================================
public class Counter {
    // =========================================================================
    // /////////////////////////////////////////////////////////////////////////
    // ///// 属性(カウンターの内部状態) ////////////////////////////////////////
    // /////////////////////////////////////////////////////////////////////////
    // =========================================================================
    /**
     * [値属性] カウント値.
     */
    // =========================================================================
    private int count = 0;
    // =========================================================================
    // /////////////////////////////////////////////////////////////////////////
    // ///// コンストラクタ (初期化) ///////////////////////////////////////////
    // /////////////////////////////////////////////////////////////////////////
    // =========================================================================
    /**
     * [コンストラクタ] 初期化する.
     */
    // =========================================================================
    public Counter() {
        count = 0;
    }
    // =========================================================================
    // /////////////////////////////////////////////////////////////////////////
    // ///// インスタンスメソッド //////////////////////////////////////////////
    // /////////////////////////////////////////////////////////////////////////
    // =========================================================================
    /**
     * カウントアップする.
     */
    // =========================================================================
    public int up() {
        count++;
        return (0);
    }
    // =========================================================================
    /**
     * カウントダウンする.
     */
    // =========================================================================
    public int down() {
        if (count > 0) {
            count--;
            return (0);
        } else {
            return (1);
        }
    }
    // =========================================================================
    /**
     * カウントをリセットする.
     */
    // =========================================================================
    public int reset() {
        count = 0;
        return (0);
    }
    // =========================================================================
    // /////////////////////////////////////////////////////////////////////////
    // ///// インスタンスメソッド: アクセッサ //////////////////////////////////
    // /////////////////////////////////////////////////////////////////////////
    // =========================================================================
    /**
     * [getter] カウント値を返す.
     *
     * @return カウント値.
     */
    // =========================================================================
    public int getCount() {
        return (count);
    }
    // =========================================================================
    /**
     * [setter] カウント値を設定する.
     *
     * @param count 設定するカウント値.
     */
    // =========================================================================
    public int setCount(int count) {
        if (count >= 0) {
            this.count = count;
            return (0);
        } else {
            return (2);
        }
    }
    // =========================================================================
}
